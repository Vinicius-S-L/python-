valor = input("Digite um valor: ")

print("o valor é numerico")
print(valor.isnumeric())

print("o valor é Alfabético ou númerico?")
print(valor.isalnum())

print("O valor é Alfabético?")
print(valor.isalpha())

print("O valor é decimal?")
print(valor.isdecimal())

print("O valor tem espaço?")
print(valor.isspace())

print("O valor tem apenas letras minusculas?")
print(valor.islower())

print("O valor tem apenas letras maiusculas?")
print(valor.isupper())